type Key = keyof (typeof dictionaries)["es"];

const LanguageContext = createContext<{
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: Key) => string;
} | null>(null);

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>("es");

  useEffect(() => {
    // One-time sync from localStorage on mount (server has no access to it,
    // so this can't be done in the initial state without a hydration mismatch).
    const stored = window.localStorage.getItem(STORAGE_KEY);
    if (stored === "es" || stored === "en") {
      // eslint-disable-next-line react-hooks/set-state-in-effect
      setLangState(stored);
    }
  }, []);

  // <html lang> is hardcoded to "en" in layout.tsx (a server component can't
  // read localStorage), but the app defaults to Spanish -- without this, a
  // screen reader applies English pronunciation rules to Spanish content on
  // every page load until the user manually toggles the switcher.
  useEffect(() => {
    document.documentElement.lang = lang;
  }, [lang]);

  function setLang(l: Lang) {
    setLangState(l);
    window.localStorage.setItem(STORAGE_KEY, l);
  }

  const t = useMemo(() => (key: Key) => dictionaries[lang][key] ?? key, [lang]);

  return <LanguageContext.Provider value={{ lang, setLang, t }}>{children}</LanguageContext.Provider>;
}

export function useTranslation() {
  const ctx = useContext(LanguageContext);
  if (!ctx) throw new Error("useTranslation must be used within LanguageProvider");
  return ctx;
}
